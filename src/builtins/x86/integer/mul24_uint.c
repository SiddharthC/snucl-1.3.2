#include <cl_cpu_ops.h>

uint mul24(uint a, uint b) {
	return a * b;
}

uint2 mul24(uint2 a, uint2 b) {
	return a * b;
}

uint3 mul24(uint3 a, uint3 b) {
	return a * b;
}

uint4 mul24(uint4 a, uint4 b) {
	return a * b;
}

uint8 mul24(uint8 a, uint8 b) {
	return a * b;
}

uint16 mul24(uint16 a, uint16 b) {
	return a * b;
}
